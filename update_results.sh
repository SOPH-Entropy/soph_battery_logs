#!/usr/bin/env bash
# Regenerates results.txt from all .run.json files
# Auto-categorises seeds by length and pattern — no manual tagging needed
set -e
BASE="$(cd "$(dirname "$0")" && pwd)"
OUT="$BASE/results.txt"

python3 - "$BASE" > "$OUT" << 'PYEOF'
import json, glob, sys, os, re
from collections import OrderedDict
from datetime import datetime, timezone

base = sys.argv[1]
suites = ["smallcrush", "crush", "bigcrush", "nist", "dieharder", "practrand"]

# ── Auto-categorise seed by hex length ──
def categorise(seed):
    n = len(seed)
    tags = []

    # Primary category by length
    if n <= 4:
        cat = "low-entropy"
    elif n <= 16:
        cat = "medium-entropy"
    elif n <= 32:
        cat = "production-128bit"
    elif n <= 64:
        cat = "production-256bit"
    else:
        cat = "oversized"

    # Auto-detect tags
    try:
        val = int(seed, 16)
        # Simple primality for small values
        if val < 10000:
            if val > 1 and all(val % i != 0 for i in range(2, min(val, 1000))):
                tags.append("prime")
    except: pass

    if all(c == '0' for c in seed):       tags.append("all-zeros")
    elif all(c == 'f' for c in seed):     tags.append("all-ones")
    elif n >= 4 and seed[:n//2] == seed[n//2:]: tags.append("repeating")

    return cat, tags

# ── Load all results ──
all_results = {}
for suite in suites:
    d = os.path.join(base, suite)
    files = sorted(glob.glob(os.path.join(d, "*.run.json")))
    if not files: continue
    results = []
    for f in files:
        with open(f) as fh:
            data = json.load(fh)
        r = data["result"]
        seed = os.path.basename(f).split("seed_")[-1].replace(".run.json", "")
        cat, tags = categorise(seed)
        results.append({
            "seed": seed, "cat": cat, "tags": tags,
            "verdict": r.get("verdict", "?"),
            "total": r.get("total_tests", r.get("total", 0)),
            "pass":  r.get("pass", r.get("passed", 0)),
            "weak":  r.get("suspect", r.get("weak", 0)),
            "fail":  r.get("fail", r.get("failed", 0)),
        })
    all_results[suite] = results

# ── Print ──
now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
print(f"SOPH V3 Battery Test Results — {now}")
print("=" * 70)

for suite in suites:
    if suite not in all_results: continue
    results = all_results[suite]

    print(f"\n{'═' * 2} {suite} ({len(results)} runs) {'═' * 2}")

    # Group by category
    groups = OrderedDict()
    for r in results:
        groups.setdefault(r["cat"], []).append(r)

    for cat, items in groups.items():
        total_t = sum(x["total"] for x in items)
        total_w = sum(x["weak"]  for x in items)
        total_f = sum(x["fail"]  for x in items)
        n_pass  = sum(1 for x in items if x["verdict"] == "PASS")

        print(f"\n  [{cat}] {len(items)} seeds — {n_pass}/{len(items)} PASS, {total_w}W {total_f}F across {total_t} tests")
        print(f"  {'Seed':<40s} {'Verdict':<8s} {'Total':>6s} {'Pass':>6s} {'Weak':>6s} {'Fail':>6s}  Tags")
        print(f"  {'─' * 40} {'─' * 7}  {'─' * 5} {'─' * 5} {'─' * 5} {'─' * 5}  {'─' * 10}")
        for r in items:
            tag_str = ", ".join(r["tags"]) if r["tags"] else ""
            seed_display = r["seed"] if len(r["seed"]) <= 38 else r["seed"][:35] + "..."
            print(f"  {seed_display:<40s} {r['verdict']:<8s} {r['total']:>6} {r['pass']:>6} {r['weak']:>6} {r['fail']:>6}  {tag_str}")

# ── Overall summary ──
print(f"\n{'═' * 2} Overall Summary {'═' * 2}")
print(f"{'Suite':<12s} {'Seeds':>6s} {'PASS':>6s} {'WARN':>6s} {'FAIL':>6s} {'Tests':>7s} {'Weak':>6s} {'Failed':>6s}")
print(f"{'─' * 12} {'─' * 6} {'─' * 6} {'─' * 6} {'─' * 6} {'─' * 7} {'─' * 6} {'─' * 6}")
grand_t = grand_w = grand_f = 0
for suite in suites:
    if suite not in all_results: continue
    items = all_results[suite]
    t = sum(x["total"] for x in items)
    w = sum(x["weak"]  for x in items)
    f = sum(x["fail"]  for x in items)
    np_ = sum(1 for x in items if x["verdict"] == "PASS")
    nw  = sum(1 for x in items if x["verdict"] == "WARN")
    nf  = sum(1 for x in items if x["verdict"] not in ("PASS", "WARN"))
    print(f"{suite:<12s} {len(items):>6d} {np_:>6d} {nw:>6d} {nf:>6d} {t:>7d} {w:>6d} {f:>6d}")
    grand_t += t; grand_w += w; grand_f += f

print(f"\nTotal: {grand_t} individual tests, {grand_w} weak ({grand_w*100/max(grand_t,1):.2f}%), {grand_f} failed ({grand_f*100/max(grand_t,1):.2f}%)")
print(f"\nGenerated by update_results.sh")
PYEOF

cat "$OUT"

# Also update seed tracker
bash "$BASE/update_seed_tracker.sh" 2>/dev/null || true

# Regenerate results.json from all .run.json files
python3 - "$BASE" << 'JSONEOF'
import json, glob, sys, os, re
from datetime import datetime, timezone

base = sys.argv[1]
suites = ["practrand", "dieharder", "nist", "smallcrush", "crush", "bigcrush"]
summary = {
    "last_updated": datetime.now(timezone.utc).isoformat(),
    "batteries": {},
    "totals": {"total_runs": 0, "total_tests": 0, "pass": 0, "suspect": 0, "fail": 0},
}

# Expected pass rates for a perfect RNG (based on statistical theory)
# These represent the probability that a run gets an all-PASS verdict
expected = {
    "practrand": {
        "expected_run_pass_rate": "~100%",
        "note": "Binary — PASS or FAIL at each data size. A true RNG should pass to 32 TiB+.",
        "sub_test_count": "~1000+ (cumulative across sizes)",
        "warn_threshold": "p < 3.2e-6 (suspect)",
    },
    "dieharder": {
        "expected_run_pass_rate": "~85-92%",
        "note": "114 tests at alpha=0.005. Each has ~1% false-positive (WEAK) rate. "
               "Expect 1-2 WEAKs per run. Multiple-comparisons: P(all pass) ≈ 0.32. "
               "A run with ≤3 WEAKs and 0 FAILs is normal.",
        "sub_test_count": 114,
        "warn_threshold": "p < 0.005 or p > 0.995",
    },
    "nist": {
        "expected_run_pass_rate": "~80-90%",
        "note": "188 sub-tests (including 148 NonOverlappingTemplate variants). "
               "Each at alpha=0.01. P(any WARN) ≈ 77% just from NonOverlappingTemplate alone. "
               "1-3 WARNs per run is expected for a perfect RNG. 0 FAILs expected.",
        "sub_test_count": 188,
        "warn_threshold": "proportion < 0.96 or p-value < 0.0001",
    },
    "smallcrush": {
        "expected_run_pass_rate": "~96-98%",
        "note": "15 tests. At alpha=0.001, P(false positive per test) ≈ 0.2%. "
               "P(all 15 pass) ≈ 97%. Occasional single suspect is normal.",
        "sub_test_count": 15,
        "warn_threshold": "p < 0.001 or p > 0.999",
    },
    "crush": {
        "expected_run_pass_rate": "~86-93%",
        "note": "144 tests at alpha=0.001. P(false positive per test) ≈ 0.2%. "
               "P(all 144 pass) ≈ 75%. 1-2 suspects per run is normal.",
        "sub_test_count": 144,
        "warn_threshold": "p < 0.001 or p > 0.999",
    },
    "bigcrush": {
        "expected_run_pass_rate": "~82-90%",
        "note": "160 tests at alpha=0.001. P(false positive per test) ≈ 0.2%. "
               "P(all 160 pass) ≈ 73%. 1-2 suspects per run is normal.",
        "sub_test_count": 160,
        "warn_threshold": "p < 0.001 or p > 0.999",
    },
}

for suite in suites:
    suite_dir = os.path.join(base, suite)
    runs = []
    if os.path.isdir(suite_dir):
        for rj in sorted(glob.glob(os.path.join(suite_dir, "*.run.json"))):
            try:
                record = json.load(open(rj))
            except Exception:
                continue
            result = record.get("result", {})
            seed = None
            gen_cmd = record.get("generator", {}).get("command", "")
            m = re.search(r"--seed-hex\s+([0-9a-fA-F]+)", gen_cmd)
            if m:
                seed = m.group(1)
            runs.append({
                "file": os.path.basename(rj),
                "date": record.get("timestamp_utc", ""),
                "seed": seed,
                "verdict": result.get("verdict", "UNKNOWN"),
                "total_tests": result.get("total_tests", 0),
                "pass": result.get("pass", 0),
                "suspect": result.get("suspect", result.get("weak", 0)),
                "fail": result.get("fail", 0),
                "pct_clean": result.get("pct_clean", 0),
                "tlmax": result.get("tlmax"),
                "reruns": result.get("reruns"),
            })

    n_runs = len(runs)
    total_tests = sum(r["total_tests"] for r in runs)
    total_pass = sum(r["pass"] for r in runs)
    total_suspect = sum(r["suspect"] for r in runs)
    total_fail = sum(r["fail"] for r in runs)

    exp = expected.get(suite, {})
    summary["batteries"][suite] = {
        "expected_run_pass_rate": exp.get("expected_run_pass_rate", "unknown"),
        "expected_note": exp.get("note", ""),
        "sub_test_count": exp.get("sub_test_count", "unknown"),
        "warn_threshold": exp.get("warn_threshold", ""),
        "total_runs": n_runs,
        "total_tests": total_tests,
        "pass": total_pass,
        "suspect": total_suspect,
        "fail": total_fail,
        "pct_clean": round(total_pass / total_tests * 100, 1) if total_tests else 0,
        "runs_pass": sum(1 for r in runs if r["verdict"] == "PASS"),
        "runs_warn": sum(1 for r in runs if r["verdict"] == "WARN"),
        "runs_fail": sum(1 for r in runs if r["verdict"] == "FAIL"),
        "runs": runs,
    }
    summary["totals"]["total_runs"] += n_runs
    summary["totals"]["total_tests"] += total_tests
    summary["totals"]["pass"] += total_pass
    summary["totals"]["suspect"] += total_suspect
    summary["totals"]["fail"] += total_fail

tr = summary["totals"]
tr["pct_clean"] = round(tr["pass"] / tr["total_tests"] * 100, 1) if tr["total_tests"] else 0

results_path = os.path.join(base, "results.json")
with open(results_path, "w") as f:
    json.dump(summary, f, indent=2)
    f.write("\n")
print(f"[update] results.json updated ({tr['total_runs']} runs, {tr['total_tests']} tests)")

# Also write totals.json (compact summary for quick consumption)
totals_path = os.path.join(base, "totals.json")
with open(totals_path, "w") as f:
    json.dump({
        "version": "V3",
        "generator": "soph_v3",
        "last_updated": summary["last_updated"],
        "totals": summary["totals"],
        "batteries": {
            suite: {
                "total_runs": b["total_runs"],
                "total_tests": b["total_tests"],
                "pass": b["pass"],
                "suspect": b["suspect"],
                "fail": b["fail"],
                "pct_clean": b["pct_clean"],
                "runs_pass": b["runs_pass"],
                "runs_warn": b["runs_warn"],
                "runs_fail": b["runs_fail"],
            }
            for suite, b in summary["batteries"].items()
            if b["total_runs"] > 0
        },
    }, f, indent=2)
    f.write("\n")
print(f"[update] totals.json updated")
JSONEOF
